export let currentLang = localStorage.getItem('appLanguage') || 'en';
export let recipeLang = localStorage.getItem('recipeLanguage') || 'source';

export function setAppLanguage(lang) {
    if (translations[lang] && lang !== currentLang) {
        currentLang = lang;
        localStorage.setItem('appLanguage', lang);
        updateUI();
    }
}

export function setRecipeLanguage(lang) {
    if (lang !== recipeLang) {
        recipeLang = lang;
        localStorage.setItem('recipeLanguage', lang);
        document.dispatchEvent(new CustomEvent('recipe-language-changed'));
    }
}

export function t(key) {
    return translations[currentLang]?.[key] || translations['en'][key] || key;
}

export function tFor(lang, key) {
    return translations[lang]?.[key] || translations['en'][key] || key;
}

export function updateUI() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (key) {
            // Hardcode icon exclusions if needed, but mostly we replace all text
            if (el.tagName === 'INPUT' && el.hasAttribute('placeholder')) {
                el.setAttribute('placeholder', t(key));
            } else if (el.childNodes.length > 0) {
                // If the element has text nodes, just replace the first text node usually,
                // or just replace textContent to be simple.
                el.textContent = t(key);
            } else {
                el.textContent = t(key);
            }
        }
    });

    // Notify other components
    document.dispatchEvent(new CustomEvent('app-language-changed'));
}

export function initI18n() {
    updateUI();
}
